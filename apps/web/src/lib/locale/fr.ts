export function frPageTitle(pathname: string): string {
  return fr.topbar.titles[pathname] ?? fr.topbar.titles["/"] ?? "Tableau de bord";
}

export function frGuestsLabel(count: number): string {
  if (count <= 0) return "—";
  return count === 1 ? `1 ${fr.common.guests}` : `${count} ${fr.common.guestsPlural}`;
}
